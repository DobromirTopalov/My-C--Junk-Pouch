/*Зад.4
Да се състави програма на C++, чрез която се извежда триъгълник от букви. Първият ред на триъгълника започва с А и завършва с въведената
главна латинска буква. Във всеки следващ ред крайният знак е с 1 по-малък (по ASCII код).*/


#include <iostream>

void draw(char a)
{
	for (char i = 'A'; i <= a; a--)
	{
		for (char j = 'A'; j <= a; j++)
		{
			std::cout << j << " ";
		}
		std::cout << "\n";
	}
}

int main()
{
	char a;
	std::cin >> a;

	draw(a);
	return 0;
}