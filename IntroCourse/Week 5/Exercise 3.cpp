/*Зад.3
Да се състави програма (C++), която извежда всички 3-цифрени естествени числа отговарящи на следното условие:
числото от десните 2 цифри да съответства на втора степен на първата цифра (квадрата). Използвайте цикъл For за обхождане на 
естествените числа от указания интервал.*/

#include <iostream>
#include <cmath>

int main()
{

	for (int i = 100; i < 1000; i++)
	{
		if (pow(i / 100, 2) == i % 100)
		{
			std::cout << i << " ";
		}
	}
	std::cout << "\n";

	return 0;
}