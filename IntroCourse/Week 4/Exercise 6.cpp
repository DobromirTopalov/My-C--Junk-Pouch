/*Зад.6
Напишете програма, която по въведени от клавиатурата цели числа x и p намира и извежда на екрана p-тата цифра на x. (Броенето започва от 1.)
броейки отзад напред.*/

#include <iostream>


int main()
{
	int x, p;
	std::cin >> x >> p;

	int k = 0;

	while (k < (p - 1))
	{
		x /= 10;
		k++;
	}
	std::cout << x%10 << "\n";

	return 0;
}